    @Override
    public void update(Observable obs, Object obj) {
        ClientesDB cliDB = (ClientesDB) obs;
        this.setTitle("Profesi√≥n: " + cliDB.getProfesion());
        graficar(cliDB.getTotalHombres(), cliDB.getTotalMujeres());
    }
    
    private void graficar(int totalHombres, int totalMujeres) {
        jProgressBarHombre.setVisible(true);
        jProgressBarMujer.setVisible(true);
        jLabelHom.setVisible(true);
        jLabelMuj.setVisible(true);
        double total = totalHombres+totalMujeres;
        double porH ;
        double porM ;
        porH = (totalHombres/total)*100;
        porM = (totalMujeres/total)*100;
        jLabelPorsentajeH.setText(""+String.format("%.2f", porH)+"%");
        jLabelPorsentajeM.setText(""+String.format("%.2f", porM)+"%");
        jProgressBarHombre.setMaximum((int)total);
        jProgressBarMujer.setMaximum((int)total);
        jProgressBarHombre.setValue(totalHombres);
        jProgressBarMujer.setValue(totalMujeres);
    }

